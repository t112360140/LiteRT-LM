#include "runtime/components/tool_use/parser_common.h"

#include <gmock/gmock.h>
#include <gtest/gtest.h>

namespace {

using ::litert::lm::StripQuotes;
using ::testing::Eq;

TEST(ParserCommonTest, StripQuotes) {
  EXPECT_THAT(StripQuotes("\"hello\""), Eq("hello"));
  EXPECT_THAT(StripQuotes("'world'"), Eq("world"));
  EXPECT_THAT(StripQuotes("\"'mixed'\""), Eq("'mixed'"));
  EXPECT_THAT(StripQuotes("'\"mixed\"'"), Eq("\"mixed\""));
  EXPECT_THAT(StripQuotes("no quotes"), Eq("no quotes"));
  EXPECT_THAT(StripQuotes(""), Eq(""));
  EXPECT_THAT(StripQuotes("\""), Eq("\""));
  EXPECT_THAT(StripQuotes("'"), Eq("'"));
  EXPECT_THAT(StripQuotes("\"a"), Eq("\"a"));
  EXPECT_THAT(StripQuotes("a\""), Eq("a\""));
  EXPECT_THAT(StripQuotes("'a"), Eq("'a"));
  EXPECT_THAT(StripQuotes("a'"), Eq("a'"));
  EXPECT_THAT(StripQuotes("\"\""), Eq(""));
  EXPECT_THAT(StripQuotes("''"), Eq(""));
}

}  // namespace
